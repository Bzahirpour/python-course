# Regular division returns a float
print(10 / 3)

# Double // will return a int
print(10 // 3)

# Modoulus returns the remainder ie 1 in this case
print(10 % 3)

# To the power of
print(10 ** 3)

# How to incrament by 3
x = 10
x = x + 3
print(x)

# Augmented assignment exactly the same as above, is x + 3 then assings it to x
x = 10
x += 3
print(x)